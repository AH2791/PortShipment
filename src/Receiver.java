public class Receiver {
    String fname;
    String lname;
    int phonenumber;
    String email;

    public Receiver() {
    }

    public Receiver(String fname, String lname, int phonenumber, String email) {
        this.fname = fname;
        this.lname = lname;
        this.phonenumber = phonenumber;
        this.email = email;
    }
}
